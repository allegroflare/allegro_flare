void Screen::render()
{
   float y_cursor = 300;
   float y_distance = 1080/7;
   int i=0;
   for (auto &save_slot : save_slots)
   {
      // HERE: Continue on this
      AllegroFlare::LoadASavedGame::SaveSlotRenderer renderer;
      renderer.render(
         save_slot,
         1920/2,
         y_cursor + y_distance*i
      );
      i++;
   }
   return;
}

